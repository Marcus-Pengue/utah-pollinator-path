// Central plant database - single source of truth
export interface Plant {
  id: string;
  commonName: string;
  scientificName: string;
  category: 'flower' | 'shrub' | 'groundcover' | 'tree';
  // Visual
  icon: string;
  color: string;
  // Size (feet)
  width: number;
  height: number;
  spacing: number;
  // Growing conditions
  sunNeeds: 'full' | 'partial' | 'shade';
  waterNeeds: 'low' | 'medium' | 'high';
  soilType: string[];
  // Bloom info
  bloomMonths: number[]; // 1-12
  bloomColor: string;
  // Pollinators
  attractsPollinators: ('bee' | 'butterfly' | 'hummingbird' | 'moth')[];
  nectarValue: 1 | 2 | 3; // 1=low, 3=high
  pollenValue: 1 | 2 | 3;
  // Water/irrigation
  gallonsPerWeek: number;
  wateringFrequency: string;
  wateringNotes: string;
  // Companion planting
  goodCompanions: string[];
  badCompanions: string[];
  // Pricing
  estimatedCost: { small: number; gallon: number; fiveGallon: number };
  // Utah specific
  utahNative: boolean;
  elevationRange: [number, number]; // feet
  usdaZones: number[];
}

