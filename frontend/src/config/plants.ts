// Helper functions
export const getPlantById = (id: string) => PLANTS.find(p => p.id === id);
export const getPlantByName = (name: string) => PLANTS.find(p => p.commonName === name);
export const getPlantsByCategory = (category: Plant['category']) => PLANTS.filter(p => p.category === category);
export const getPlantsForPollinator = (pollinator: 'bee' | 'butterfly' | 'hummingbird' | 'moth') => 
  PLANTS.filter(p => p.attractsPollinators.includes(pollinator));
export const getBloomingPlants = (month: number) => PLANTS.filter(p => p.bloomMonths.includes(month));
export const getCompanions = (plantId: string) => {
  const plant = getPlantById(plantId);
  return plant?.goodCompanions.map(name => getPlantByName(name)).filter(Boolean) || [];
};
