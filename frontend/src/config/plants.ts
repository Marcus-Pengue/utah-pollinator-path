export function getPlantById(id: string): Plant | undefined {
  return PLANTS.find(p => p.id === id);
}

export function getPlantsByCategory(category: Plant['category']): Plant[] {
  return PLANTS.filter(p => p.category === category);
}

export function getPlantsByWaterZone(zone: 0 | 1 | 2 | 3 | 4): Plant[] {
  return PLANTS.filter(p => p.localscapesZone === zone);
}

export function getPlantsByBloomMonth(month: number): Plant[] {
  return PLANTS.filter(p => p.bloomMonths.includes(month));
}

export function getPlantsForPollinator(pollinator: string): Plant[] {
  return PLANTS.filter(p => 
    p.attractsPollinators.includes(pollinator) ||
    p.specificPollinators.some(sp => sp.toLowerCase().includes(pollinator.toLowerCase()))
  );
}

export function getHostPlants(): Plant[] {
  return PLANTS.filter(p => p.hostPlant && p.hostPlant.length > 0);
}

export function getNativePlants(): Plant[] {
  return PLANTS.filter(p => p.utahNative);
}

export function getEarlySpringBloomers(): Plant[] {
  return PLANTS.filter(p => p.bloomMonths.includes(3) || p.bloomMonths.includes(4));
}

export function getLateFallBloomers(): Plant[] {
  return PLANTS.filter(p => p.bloomMonths.includes(10) || p.bloomMonths.includes(11));
}

export function getLowestWaterPlants(): Plant[] {
  return PLANTS.filter(p => p.localscapesZone === 0);
}

export function getCompanions(plantId: string): Plant[] {
  const plant = getPlantById(plantId);
  if (!plant) return [];
  return plant.goodCompanions
    .map(id => getPlantById(id))
    .filter((p): p is Plant => p !== undefined);
}

export function calculateHabitatScore(plantIds: string[]): number {
  const plants = plantIds.map(id => getPlantById(id)).filter((p): p is Plant => p !== undefined);
  if (plants.length === 0) return 0;
  
  let score = 0;
  
  // Native plant bonus
  const nativeRatio = plants.filter(p => p.utahNative).length / plants.length;
  score += nativeRatio * 25;
  
  // Nectar/pollen value
  const avgNectar = plants.reduce((sum, p) => sum + p.nectarValue, 0) / plants.length;
  const avgPollen = plants.reduce((sum, p) => sum + p.pollenValue, 0) / plants.length;
  score += ((avgNectar + avgPollen) / 2) * 10;
  
  // Bloom coverage (months covered)
  const monthsCovered = new Set(plants.flatMap(p => p.bloomMonths)).size;
  score += (monthsCovered / 12) * 20;
  
  // Host plant bonus
  const hostPlants = plants.filter(p => p.hostPlant && p.hostPlant.length > 0).length;
  score += Math.min(hostPlants * 5, 15);
  
  // Water efficiency bonus
  const avgWaterZone = plants.reduce((sum, p) => sum + p.localscapesZone, 0) / plants.length;
  score += (4 - avgWaterZone) * 5;
  
  return Math.round(Math.min(score, 100));
}

